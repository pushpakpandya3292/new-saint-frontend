// @ is an alias to /src
import Header from "@/components/Header.vue";
import Footer from "@/components/Footer.vue";
import $ from "jquery";
export default {
  name: "Home",
  components: {
    Header,
    Footer,
  },
  mounted() {
    $(window).scroll(function () {
      var scrollTop = $(this).scrollTop();
      $(".page-title-opacity-text").css({
        opacity: function () {
          var elementHeight = $(this).height();
          // return 1 - (elementHeight + scrollTop) / elementHeight;
          return 2 - (elementHeight + scrollTop) / elementHeight + 0;
        },
      });
    });
    $(window).scroll(function () {
      if ($(this).scrollTop() > 350) {
        $(".page-title-fix").addClass("d-none");
      } else {
        $(".page-title-fix").removeClass("d-none");
      }
    });
    $(window).scroll(function () {
      if ($(this).scrollTop() > 400) {
        $("#page-title-image-inner").addClass("section-full-width");
      } else {
        $("#page-title-image-inner").removeClass("section-full-width");
      }
    });
    $(window).scroll(function () {
      var scrollPosition = $(window).scrollTop();
      var pagetitleimageinner = $(".page-title-image-inner").offset().top;
      var drivemoresection = $(".drive-more-section").offset().top;
      var robustvideosection = $(".robust-video-section").offset().top;
      var usecasessection = $(".use-cases-section").offset().top;
      if (scrollPosition >= pagetitleimageinner) {
        $(".header-section").addClass("light-header");
        $("#page-title-image-inner").addClass("section-full-width");
      } else {
        $(".header-section").removeClass("light-header");
        $("#page-title-image-inner").removeClass("section-full-width");
      }
      if (scrollPosition >= drivemoresection) {
        $(".header-section").removeClass("light-header");
        $(".header-section").addClass("dark-header-with-border");
      } else {
        // $(".header-section").removeClass("dark-header-with-border");
      }
      if (scrollPosition >= robustvideosection) {
        $(".header-section").addClass("light-header");
        $(".header-section").removeClass("dark-header-with-border");
      } else {
        $(".header-section").removeClass("light-header");
      }
      if (scrollPosition >= usecasessection) {
        $(".header-section").removeClass("light-header");
        $(".header-section").addClass("dark-header-with-border");
      } else {
        $(".header-section").removeClass("dark-header-with-border");
      }
    });
    $(window).scroll(function () {
      var scrollPosition = $(window).scrollTop();
      var usecasesdetailone = $(
        ".use-cases-detail-one .use-cases-detail-inner"
      ).offset().top;
      if (scrollPosition >= usecasesdetailone) {
        $(".use-cases-video-two").removeClass("d-none");
        $(".use-cases-video-one").addClass("d-none");
      } else {
        $(".use-cases-video-two").addClass("d-none");
        $(".use-cases-video-one").removeClass("d-none");
      }
    });
    $(window).scroll(function () {
      var scrollPosition = $(window).scrollTop();
      var usecasesdetailtwo = $(
        ".use-cases-detail-two .use-cases-detail-inner"
      ).offset().top;
      if (scrollPosition >= usecasesdetailtwo) {
        $(".use-cases-video-three").removeClass("d-none");
        $(".use-cases-video-two").addClass("d-none");
      } else {
        $(".use-cases-video-three").addClass("d-none");
      }
    });
    $(window).scroll(function () {
      var scrollPosition = $(window).scrollTop();
      var usecasesdetailthree = $(
        ".use-cases-detail-three .use-cases-detail-inner"
      ).offset().top;
      if (scrollPosition >= usecasesdetailthree) {
        $(".use-cases-video-four").removeClass("d-none");
        $(".use-cases-video-three").addClass("d-none");
      } else {
        $(".use-cases-video-four").addClass("d-none");
      }
    });
    this.$nextTick(function () {
      setTimeout(function () {
        var hash = "#" + window.location.hash.substring(1);
        const data = $(hash);
        console.log("data", data);
        $("html, body").animate({
          scrollTop: $(data).position().top,
        });
      }, 100);
    });
  },
};
</script>
